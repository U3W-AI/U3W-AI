// 在文件顶部添加 context 定义
const viewsContext = require.context('@/views', true, /\.vue$/)

export const loadView = (view) => {
  return () => {
    return new Promise((resolve, reject) => {
      try {
        // 构造相对路径
        const relativePath = `./${view}.vue`
        const keys = viewsContext.keys()
        const matchedKey = keys.find(key => key === relativePath)
        
        if (matchedKey) {
          const component = viewsContext(matchedKey)
          // 处理 ES Module 默认导出
          resolve(component.default || component)
        } else {
          // 如果 context 中找不到，则尝试动态导入
          import(`@/views/${view}.vue`)
            .then(module => resolve(module.default || module))
            .catch(reject)
        }
      } catch (error) {
        // 出错时回退到动态导入
        import(`@/views/${view}.vue`)
          .then(module => resolve(module.default || module))
          .catch(reject)
      }
    })
  }
}
export default permission
